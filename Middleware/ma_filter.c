void MoveAverageFilter_Init(MA_FILTER_T *pMaf)
{
    memset(pMaf, 0, sizeof(MA_FILTER_T) / sizeof(uint8_t));

    pMaf->index = -1;
    pMaf->sum   = 0;
}

EM_SAMPLE MoveAverageFilter_Insert(MA_FILTER_T *pMaf, EM_SAMPLE xn)
{
    EM_SAMPLE yn;

    if (pMaf->index == -1)
    {
        for (int i = 0; i < MVF_LENGTH; i++)
        {
            pMaf->buffer[i] = xn;
        }
        pMaf->sum   = xn * MVF_LENGTH;
        pMaf->index = 0;
    }
    else
    {
        pMaf->sum -= pMaf->buffer[pMaf->index];
        pMaf->buffer[pMaf->index] = xn;
        pMaf->sum += xn;
        pMaf->index++;
        if (pMaf->index >= MVF_LENGTH)
        {
            pMaf->index = 0;
        }
    }
    yn = pMaf->sum / MVF_LENGTH;

    //    if (yn < 0.0005f)
    //    {
    //        yn = 0.0f;
    //    }

    return yn;
}

void MoveAverageFilter_ClearBuffer(MA_FILTER_T *pMaf)
{
    for (int i = 0; i < MVF_LENGTH; ++i)
    {
        pMaf->buffer[i] = 0;
    }
    pMaf->sum   = 0;
    pMaf->index = 0;
}

